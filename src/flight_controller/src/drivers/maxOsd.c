static void UpdateCharMap(void)
{
    int     x;             //set
    int     y;             //set
    uint8_t character[54]; //set

    maxOsdRecord.status = OSD_STATUS_LOADING_CHAR_MAP;

    for(x = 0; x < 255; x++)
    {
        for(y = 0; y < 54; y++)
        {
            character[y] = (uint8_t)(charmap[(64*x)+y]);
        }
        WriteNvm(x, character);
    }

    maxOsdRecord.status = OSD_STATUS_DISABLED;
    
}

//blocking
static int WriteNvm(uint8_t charAddress, uint8_t character[])
{
    uint8_t x; //set

    MaxOsdSetVidoOnOff(0);

    // set start address high
    inlineDigitalLo(ports[board.maxOsd[0].csPort], board.maxOsd[0].csPin);
    MaxSendReceiveCommand(MAX7456ADD_CMAH, charAddress, 1, 0);

    for(x = 0; x < NVRAM_SIZE; x++) // write out 54 bytes of character to shadow ram
    {
        MaxSendReceiveCommand(MAX7456ADD_CMAL, x, 1, 0);            //set start address low
        MaxSendReceiveCommand(MAX7456ADD_CMDI, character[x], 1, 0); //send char
    }

    // transfer 54 bytes from shadow ram to NVM
    MaxSendReceiveCommand(MAX7456ADD_CMM, WRITE_NVRAM, 1, 0);

    inlineDigitalLo(ports[board.maxOsd[0].csPort], board.maxOsd[0].csPin);
    DelayMs(1);
    //blocking while loop
    //wait until bit 5 in the status register returns to 0 (12ms)
    for(x=0; x<6; x++)
    {
        if ( (MaxSendReceiveCommand(MAX7456ADD_STAT, 0x00, 1, 1) & STATUS_REG_NVR_BUSY) == 0x00 )
        {
            break;
        }
        DelayMs(4);
    }

    if(x==5)
        return(1);
    else
        return(0);
}

int CheckForVideoSignal(void)
{
    uint8_t spiReturnData; //set
    int     x;             //set
    //look for video signal
    //B00000011 = 3


    if (maxOsdRecord.commandStatus != OSD_COMMAND_STATUS_IDLE)
        return(0);
 
    spiReturnData = MaxSendReceiveCommand(MAX7456ADD_STAT, 0xFF, 1, 1);
    if ((3 & spiReturnData)) //do we have a signal?
    {
        //video found!
        //check if signal is PAL or NTSC
        //B00000001 = 1
        //B00000010 = 2
        if ((1 & spiReturnData) == 1) //PAL
        {
            maxOsdRecord.videoMode     = VIDEO_MODE_PAL;
            maxOsdRecord.osdScreenSize = SCREEN_SIZE_PAL;
            maxOsdRecord.osdRows       = VIDEO_LINES_PAL;
            maxOsdRecord.osdColumnSize = (maxOsdRecord.osdScreenSize / maxOsdRecord.osdRows);
            MaxSendReceiveCommand(MAX7456ADD_OSDM, 0x00, 1, 1);
            for(x = 0; x < maxOsdRecord.osdRows; x++)
                MaxSendReceiveCommand(MAX7456ADD_RB0+x, BWBRIGHTNESS, 1, 1);
            return(1);
        }
        else if((2 & spiReturnData) == 2) //NTSC
        {
            maxOsdRecord.videoMode     = VIDEO_MODE_NTSC;
            maxOsdRecord.osdScreenSize = SCREEN_SIZE_NTSC;
            maxOsdRecord.osdRows       = VIDEO_LINES_NTSC;
            maxOsdRecord.osdColumnSize = (maxOsdRecord.osdScreenSize / maxOsdRecord.osdRows);
            MaxSendReceiveCommand(MAX7456ADD_OSDM, 0x00, 1, 1);
            for(x = 0; x < maxOsdRecord.osdRows; x++)
                MaxSendReceiveCommand(MAX7456ADD_RB0+x, BWBRIGHTNESS, 1, 1);
            return(1);
        }
        
    }

    return(0);
}

static int FillMaxOsdBuffer(void)
{
    int currentLine;
    uint32_t temp, remainderUsed;

    //set the line to update
    volatile uint8_t *buffPtr;

    for(currentLine=0;currentLine<(maxOsdRecord.osdRows);currentLine++)
    {
        //set the line
        buffPtr = &maxOsdRecord.screenBuffer[maxOsdRecord.osdColumnSize * currentLine];
        memset((char *)buffPtr, ' ', maxOsdRecord.osdColumnSize);
        switch(currentLine)
        {
            case 0:
                snprintf((char *)buffPtr, maxOsdRecord.osdColumnSize, " RF1 FT %lu                              ", (armedTime / 1000));
                break;
            case 11:
                if (averageVoltage < 0.5)
                {
                    temp = 0;
                    remainderUsed = 0;
                }
                else
                {
                    temp = (uint32_t)(averageVoltage);
                    remainderUsed = (uint32_t)((averageVoltage - (float)temp) * 100);
                }
                snprintf((char *)buffPtr, maxOsdRecord.osdColumnSize, " Volt: %lu.%lu                            ", temp, remainderUsed );
                break;
            case 12:
                if (adcCurrent < 0.5)
                {
                    temp = 0;
                    remainderUsed = 0;
                }
                else
                {
                    temp = (uint32_t)(adcCurrent);
                    remainderUsed = (uint32_t)((adcCurrent - (float)temp) * 100);
                }
                snprintf((char *)buffPtr, maxOsdRecord.osdColumnSize, " Curr: %lu.%lu                            ", temp, remainderUsed );
            default:
                break;

        }

    }

    return(0);
}

int HandleMaxOsd(void)
{
    //only run every 100 ms
    static uint32_t lastTimeRun = 0;

    if(mainConfig.telemConfig.telemRfOsd != TELEM_INTERNAL_OSD)
        return(0);

    if ( (InlineMillis() - lastTimeRun) < 32)
        return(0);

    switch(maxOsdRecord.osdUpdateStatus)
    {
        case OSD_STATUS_UPDATE_IDLE:
            maxOsdRecord.osdUpdateStatus = OSD_STATUS_REQUEST_UPDATE;
            return 0;
            break;
        case OSD_STATUS_REQUEST_UPDATE:
        case OSD_STATUS_UPDATING:
            return 0;
            break;
        case OSD_STATUS_UPDATE_APROVED:
            break;

    }

    if ( (maxOsdRecord.type == OSD_TYPE_SPI) && (maxOsdRecord.videoMode == VIDEO_MODE_UNKNOWN) )
    {
        CheckForVideoSignal();
    }
    else
    {

        if (maxOsdRecord.status == OSD_STATUS_DISABLED && maxOsdRecord.requestScreen != OSD_REQUEST_SCREEN_OFF)
            MaxOsdSetVidoOnOff(1);
        else if (maxOsdRecord.status == OSD_STATUS_ENABLED && maxOsdRecord.requestScreen == OSD_REQUEST_SCREEN_OFF)
            MaxOsdSetVidoOnOff(0);
     
        FillMaxOsdBuffer();
        DrawScreen(0);
    }

    lastTimeRun = InlineMillis();
    maxOsdRecord.osdUpdateStatus=OSD_STATUS_UPDATE_IDLE;
    return(1);
}

int InitMaxOsd(void)
{

    //return(0);
    int x; //set
    uint8_t spiReturnData; //set

    if(mainConfig.telemConfig.telemRfOsd != TELEM_INTERNAL_OSD)
        return(0);
    //disabled for now
    //
    //reset structure
    maxOsdRecord.status           = OSD_STATUS_UNKNOWN;
    maxOsdRecord.type             = OSD_TYPE_UNKNOWN;
    maxOsdRecord.videoMode        = VIDEO_MODE_UNKNOWN;
    maxOsdRecord.lastComandMillis = InlineMillis();
    maxOsdRecord.commandStatus    = OSD_COMMAND_STATUS_IDLE;
    maxOsdRecord.osdRows          = 0;
    maxOsdRecord.osdScreenSize    = 0;
    maxOsdRecord.requestScreen    = OSD_REQUEST_SCREEN_1;
    maxOsdRecord.osdUpdateStatus  = OSD_STATUS_UPDATE_IDLE;

    //Attatch OSD chip reset to PB8
    //Attatch OSD Chip LOS to PC0
    //Attatch OSD chip Vsync to PB9
    GPIO_InitTypeDef GPIO_InitStruct;
 
    //init hard rest pin
    GPIO_InitStruct.Pin   = GPIO_PIN_8;
    GPIO_InitStruct.Mode  = GPIO_MODE_OUTPUT_PP;
    GPIO_InitStruct.Pull  = GPIO_PULLUP;
    GPIO_InitStruct.Speed = GPIO_SPEED_FREQ_VERY_HIGH;

    //start hi
    inlineDigitalHi(ports[ENUM_PORTB], GPIO_PIN_8);
    //init pin
    HAL_GPIO_Init(ports[ENUM_PORTB], &GPIO_InitStruct);
    //go low
    inlineDigitalLo(ports[ENUM_PORTB], GPIO_PIN_8);
    //hold 55 ms
    DelayMs(55);
    //go high
    inlineDigitalHi(ports[ENUM_PORTB], GPIO_PIN_8);
    //give time for chip to recover
    DelayMs(2);

    //fill buffers
    for (x=OSD_SCREEN_BUFFER_SIZE-1;x>=0;x--)
    {
        maxOsdRecord.screenBuffer[x] = OSD_EMPTY_CHAR;
        maxOsdRecord.shadowBuffer[x] = OSD_EMPTY_CHAR_SHADOW_START;
    }

    //set osd text
    strcpy((char *)maxOsdRecord.screenBuffer, " RaceFlight OSD ");
	if (board.maxOsd[0].enabled == 2)
    {

        //init the CS pin, SPI is already in use so no need to double init
        GPIO_InitStruct.Pin   = board.maxOsd[0].csPin;
        GPIO_InitStruct.Mode  = GPIO_MODE_OUTPUT_PP;
        GPIO_InitStruct.Pull  = GPIO_PULLUP;
        GPIO_InitStruct.Speed = GPIO_SPEED_FREQ_VERY_HIGH;

        inlineDigitalHi(ports[board.maxOsd[0].csPort], board.maxOsd[0].csPin);
        HAL_GPIO_Init(ports[board.maxOsd[0].csPort], &GPIO_InitStruct);
        inlineDigitalHi(ports[board.maxOsd[0].csPort], board.maxOsd[0].csPin);
        DelayMs(55);

        //soft reset
        maxOsdRecord.status = OSD_STATUS_SPI_RESETTING;
        spiReturnData = MaxSendReceiveCommand(MAX7456ADD_VM0, MAX7456_RESET, 1, 1);

        if (!spiReturnData)
        {
            maxOsdRecord.status = OSD_STATUS_DISABLED;
            maxOsdRecord.type   = OSD_TYPE_SPI;
            CheckForVideoSignal();

        }
        else
        {
            maxOsdRecord.status = OSD_STATUS_UNKNOWN;
        }
    }
    else if (board.maxOsd[0].enabled == 1)
    {

        //TODO: Check for DMA conflicts
        if (board.dmasSpi[board.spis[board.maxOsd[0].spiNumber].RXDma].enabled)
        {
            memcpy( &board.dmasActive[board.spis[board.maxOsd[0].spiNumber].RXDma], &board.dmasSpi[board.spis[board.maxOsd[0].spiNumber].RXDma], sizeof(board_dma) );
        }
        if (board.dmasSpi[board.spis[board.maxOsd[0].spiNumber].TXDma].enabled)
        {
            memcpy( &board.dmasActive[board.spis[board.maxOsd[0].spiNumber].TXDma], &board.dmasSpi[board.spis[board.maxOsd[0].spiNumber].TXDma], sizeof(board_dma) );
        }

        //currently blocks
        if (SpiInit(board.maxOsd[0].spiFastBaud))
        {
            //always send soft reset at init
            maxOsdRecord.status = OSD_STATUS_SPI_RESETTING;
            spiReturnData = MaxSendReceiveCommand(MAX7456ADD_VM0, MAX7456_RESET, 1, 1);
            if (!spiReturnData)
            {
                maxOsdRecord.status = OSD_STATUS_DISABLED;
                maxOsdRecord.type   = OSD_TYPE_SPI;
                //should return 0

                //if (!mainConfig.gyroConfig.boardCalibrated)
                //{
                //    maxOsdRecord.status = OSD_STATUS_LOADING_CHAR_MAP;
                //    UpdateCharMap();
                //    maxOsdRecord.status = OSD_STATUS_DISABLED;
                //    //board not calibrated so we force flash the MAX chip's char map
                //}

                CheckForVideoSignal();

            }
            else
            {
                maxOsdRecord.status = OSD_STATUS_UNKNOWN;
            }

        }
        else
        {
            return(0);
        }

    }
    else
    {
        maxOsdRecord.status = OSD_TYPE_UNKNOWN;
    }


    return(0);
}

static void DrawScreen(int force)
{

    int        x; //set
    int        bufferLength = 0;
    static int position = 0; 

    if(maxOsdRecord.commandStatus != OSD_COMMAND_STATUS_IDLE)
        return;

    if( HAL_SPI_GetState(&spiHandles[board.spis[board.maxOsd[0].spiNumber].spiHandle]) == HAL_SPI_STATE_READY )
    {
        for (x=0; x< MAX_CHARS2UPDATE; x++)
        {
            if(force)
            {
                txBuffer[bufferLength++] = MAX7456ADD_DMAH;
                txBuffer[bufferLength++] = position >> 8;
                txBuffer[bufferLength++] = MAX7456ADD_DMAL;
                txBuffer[bufferLength++] = position & 0xff;
                txBuffer[bufferLength++] = MAX7456ADD_DMDI;
                txBuffer[bufferLength++] = maxOsdRecord.screenBuffer[position];
                maxOsdRecord.shadowBuffer[position] = maxOsdRecord.screenBuffer[position];
                x++;
            }
            else if (maxOsdRecord.screenBuffer[position] != maxOsdRecord.shadowBuffer[position])
            {
                txBuffer[bufferLength++] = MAX7456ADD_DMAH;
                txBuffer[bufferLength++] = position >> 8;
                txBuffer[bufferLength++] = MAX7456ADD_DMAL;
                txBuffer[bufferLength++] = position & 0xff;
                txBuffer[bufferLength++] = MAX7456ADD_DMDI;
                txBuffer[bufferLength++] = maxOsdRecord.screenBuffer[position];
                maxOsdRecord.shadowBuffer[position] = maxOsdRecord.screenBuffer[position];
                x++;
            }

            if (++position >= maxOsdRecord.osdScreenSize)
            {
                position = 0;
                break;
            }
        }

        if (bufferLength)
        {
            maxOsdRecord.commandStatus = OSD_COMMAND_STATUS_SPI_SENDING_BLK;
            inlineDigitalLo(ports[board.maxOsd[0].csPort], board.maxOsd[0].csPin);
            HAL_SPI_TransmitReceive(&spiHandles[board.spis[board.maxOsd[0].spiNumber].spiHandle], txBuffer, rxBuffer, bufferLength, 500);
            inlineDigitalHi(ports[board.maxOsd[0].csPort], board.maxOsd[0].csPin);
            maxOsdRecord.commandStatus = OSD_COMMAND_STATUS_IDLE;
        }
    }
}

static uint8_t MaxSendReceiveCommand(uint8_t address, uint8_t data, int blocking, int autoEnable)
{

    uint8_t returnData = 0;
    //volatile uint8_t returnData1 = 0;
    //volatile uint8_t returnData2 = 0;
    //volatile uint8_t returnData3 = 0;
    //volatile uint8_t returnData4 = 0;

    bzero(txBuffer, sizeof(txBuffer));
    bzero(rxBuffer, sizeof(rxBuffer));

    txBuffer[0] = address;
    txBuffer[1] = data;

    blocking = 1;
    if( HAL_SPI_GetState(&spiHandles[board.spis[board.maxOsd[0].spiNumber].spiHandle]) == HAL_SPI_STATE_READY )
    {
        if(autoEnable)
            inlineDigitalLo(ports[board.maxOsd[0].csPort], board.maxOsd[0].csPin);
        if (blocking)
        {
            maxOsdRecord.commandStatus = OSD_COMMAND_STATUS_SPI_SENDING_BLK;
            HAL_SPI_TransmitReceive(&spiHandles[board.spis[board.maxOsd[0].spiNumber].spiHandle], txBuffer, rxBuffer, 2, 500);
            returnData = rxBuffer[1];
            //returnData1 = rxBuffer[0];;
            //returnData2 = rxBuffer[1];;
            //returnData3 = rxBuffer[2];;
            //returnData4 = rxBuffer[3];;
            if(autoEnable)
                inlineDigitalHi(ports[board.maxOsd[0].csPort], board.maxOsd[0].csPin);
            maxOsdRecord.commandStatus = OSD_COMMAND_STATUS_IDLE;
            return(returnData);
        }
        else
        {
            inlineDigitalLo(ports[board.maxOsd[0].csPort], board.maxOsd[0].csPin);
            maxOsdRecord.commandStatus = OSD_COMMAND_STATUS_SPI_SENDING_DMA;
            HAL_SPI_TransmitReceive_DMA(&spiHandles[board.spis[board.maxOsd[0].spiNumber].spiHandle], txBuffer, rxBuffer, 2);
        }
    }

    return(-1);
}

