uint8_t SmCrc8(const uint8_t * ptr, uint8_t len)
{
	uint8_t crc = 0;

	for (uint8_t i=0; i<len; i++)
	{
		crc = crc8tab[crc ^ *ptr++];
	}
	return(crc);
}

//4 bit crc for a 16 bit packet
uint8_t CRC4_12(uint16_t crcData)
{
    uint16_t crc = 0;
    for (int i = 0; i < 3; i++)
    {
        crc ^=  crcData;   // xor data by nibbles
        crcData >>= 4;
    }
    return(crc & 0xf);
}

